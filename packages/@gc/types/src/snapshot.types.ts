/**
 * Типы снапшотов эмоций для Garden Calm
 */

import { Emotion } from './emotion.types';

/**
 * Интерфейс для представления снапшота эмоционального состояния пользователя
 */
export interface EmotionSnapshot {
  /**
   * Уникальный идентификатор пользователя
   */
  userId: string;
  
  /**
   * Массив эмоций, обнаруженных в сообщениях пользователя
   */
  emotions: Emotion[];
  
  /**
   * Доминирующая эмоция в текущем снапшоте
   */
  dominantEmotion: Emotion;
  
  /**
   * Временная метка создания снапшота
   */
  timestamp: Date;
  
  /**
   * Количество сообщений, на основе которых создан снапшот
   */
  messageCount: number;
  
  /**
   * Временная метка последнего обновления снапшота
   */
  lastUpdated: Date;
  
  /**
   * Сгенерированный промпт для AI на основе эмоционального состояния (опционально)
   */
  prompt?: string;
}
