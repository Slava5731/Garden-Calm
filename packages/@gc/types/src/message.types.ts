/**
 * Типы сообщений для Garden Calm
 */

import { Emotion } from './emotion.types';

/**
 * Интерфейс для представления сообщения в чате
 */
export interface Message {
  /**
   * Уникальный идентификатор сообщения
   */
  id: string;
  
  /**
   * Идентификатор пользователя, отправившего сообщение
   */
  userId: string;
  
  /**
   * Содержимое сообщения
   */
  content: string;
  
  /**
   * Роль отправителя сообщения (пользователь или ассистент)
   */
  role: 'user' | 'assistant';
  
  /**
   * Временная метка отправки сообщения
   */
  timestamp: Date;
  
  /**
   * Определенная эмоция в сообщении (опционально)
   */
  emotion?: Emotion;
  
  /**
   * Метаданные сообщения (опционально)
   */
  metadata?: {
    /**
     * Время обработки сообщения в миллисекундах
     */
    processingTime?: number;
    
    /**
     * Модель, использованная для генерации ответа
     */
    modelUsed?: string;
  };
}
